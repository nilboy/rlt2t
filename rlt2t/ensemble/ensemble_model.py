@dataclass
class EnsembleModelOutput(BaseModelOutput):
    """
    Base class for model's outputs, with potential hidden states and attentions.

    Args:
        last_hidden_state (`torch.FloatTensor` of shape `(batch_size, sequence_length, hidden_size)`):
            Sequence of hidden-states at the output of the last layer of the model.
        hidden_states (`tuple(torch.FloatTensor)`, *optional*, returned when `output_hidden_states=True` is passed or when `config.output_hidden_states=True`):
            Tuple of `torch.FloatTensor` (one for the output of the embeddings, if the model has an embedding layer, +
            one for the output of each layer) of shape `(batch_size, sequence_length, hidden_size)`.

            Hidden-states of the model at the output of each layer plus the optional initial embedding outputs.
        attentions (`tuple(torch.FloatTensor)`, *optional*, returned when `output_attentions=True` is passed or when `config.output_attentions=True`):
            Tuple of `torch.FloatTensor` (one for each layer) of shape `(batch_size, num_heads, sequence_length,
            sequence_length)`.

            Attentions weights after the attention softmax, used to compute the weighted average in the self-attention
            heads.
    """

    last_hidden_state: torch.FloatTensor = None
    hidden_states: Optional[Tuple[Tuple[torch.FloatTensor]]] = None
    attentions: Optional[Tuple[Tuple[torch.FloatTensor]]] = None


class EnsembleBartEncoder(BartPretrainedModel):
    def __init__(self, encoders, config):
        super().__init__(config)
        self.encoder_list = nn.ModuleList(encoders)

    def forward(self,
                input_ids: torch.LongTensor = None,
                attention_mask: Optional[torch.Tensor] = None,
                head_mask: Optional[torch.Tensor] = None,
                inputs_embeds: Optional[torch.FloatTensor] = None,
                output_attentions: Optional[bool] = None,
                output_hidden_states: Optional[bool] = None,
                return_dict: Optional[bool] = None) -> Union[Tuple, BaseModelOutput]:
        outputs_list = []
        for i in range(len(self.encoder_list)):
            outputs_list.append(self.encoder_list[i](input_ids, attention_mask,
                                                     head_mask,
                                                     inputs_embeds,
                                                     output_attentions,
                                                     output_hidden_states,
                                                     return_dict))
        if not isinstance(outputs_list[0], tuple):
            outputs_list = [
                (outputs.last_hidden_state, outputs.hidden_states, outputs.attentions)
                for outputs in outputs_list
            ]

        merge_outputs = [[] for _ in range(len(outputs_list[0]))]
        for outputs in outputs_list:
            for idx, item in enumerate(outputs):
                merge_outputs[idx].append(item)
        merge_outputs = [tuple(item) for item in merge_outputs]
        if not return_dict:
            return tuple(merge_outputs)
        return EnsembleModelOutput(
            last_hidden_state=torch.cat(merge_outputs[0], axis=-1),
            hidden_states=merge_outputs[1],
            attentions=merge_outputs[2]
        )

@dataclass
class EnsembleModelOutputWithPastAndCrossAttentions(BaseModelOutputWithPastAndCrossAttentions):
    """
    Base class for model's outputs that may also contain a past key/values (to speed up sequential decoding).

    Args:
        last_hidden_state (`torch.FloatTensor` of shape `(batch_size, sequence_length, hidden_size)`):
            Sequence of hidden-states at the output of the last layer of the model.

            If `past_key_values` is used only the last hidden-state of the sequences of shape `(batch_size, 1,
            hidden_size)` is output.
        past_key_values (`tuple(tuple(torch.FloatTensor))`, *optional*, returned when `use_cache=True` is passed or when `config.use_cache=True`):
            Tuple of `tuple(torch.FloatTensor)` of length `config.n_layers`, with each tuple having 2 tensors of shape
            `(batch_size, num_heads, sequence_length, embed_size_per_head)`) and optionally if
            `config.is_encoder_decoder=True` 2 additional tensors of shape `(batch_size, num_heads,
            encoder_sequence_length, embed_size_per_head)`.

            Contains pre-computed hidden-states (key and values in the self-attention blocks and optionally if
            `config.is_encoder_decoder=True` in the cross-attention blocks) that can be used (see `past_key_values`
            input) to speed up sequential decoding.
        hidden_states (`tuple(torch.FloatTensor)`, *optional*, returned when `output_hidden_states=True` is passed or when `config.output_hidden_states=True`):
            Tuple of `torch.FloatTensor` (one for the output of the embeddings, if the model has an embedding layer, +
            one for the output of each layer) of shape `(batch_size, sequence_length, hidden_size)`.

            Hidden-states of the model at the output of each layer plus the optional initial embedding outputs.
        attentions (`tuple(torch.FloatTensor)`, *optional*, returned when `output_attentions=True` is passed or when `config.output_attentions=True`):
            Tuple of `torch.FloatTensor` (one for each layer) of shape `(batch_size, num_heads, sequence_length,
            sequence_length)`.

            Attentions weights after the attention softmax, used to compute the weighted average in the self-attention
            heads.
        cross_attentions (`tuple(torch.FloatTensor)`, *optional*, returned when `output_attentions=True` and `config.add_cross_attention=True` is passed or when `config.output_attentions=True`):
            Tuple of `torch.FloatTensor` (one for each layer) of shape `(batch_size, num_heads, sequence_length,
            sequence_length)`.

            Attentions weights of the decoder's cross-attention layer, after the attention softmax, used to compute the
            weighted average in the cross-attention heads.
    """

    last_hidden_state: Tuple[torch.FloatTensor] = None
    past_key_values: Optional[Tuple[Tuple[Tuple[torch.FloatTensor]]]] = None
    hidden_states: Optional[Tuple[Tuple[torch.FloatTensor]]] = None
    attentions: Optional[Tuple[Tuple[torch.FloatTensor]]] = None
    cross_attentions: Optional[Tuple[Tuple[torch.FloatTensor]]] = None


class EnsembleBartDecoder(BartPretrainedModel):
    def __init__(self, decoders, config):
        super().__init__(config)
        self.decoder_list = nn.ModuleList(decoders)
        hidden_nums = [
            decoder.config.d_model
            for decoder in decoders
        ]
        self.accum_hidden_nums = [0]
        for item in hidden_nums:
            self.accum_hidden_nums.append(self.accum_hidden_nums[-1] + item)


    def forward(
        self,
        input_ids: torch.LongTensor = None,
        attention_mask: Optional[torch.Tensor] = None,
        encoder_hidden_states: Optional[Tuple[torch.FloatTensor]] = None,
        encoder_attention_mask: Optional[torch.LongTensor] = None,
        head_mask: Optional[torch.Tensor] = None,
        cross_attn_head_mask: Optional[torch.Tensor] = None,
        past_key_values: Optional[List[torch.FloatTensor]] = None,
        inputs_embeds: Optional[torch.FloatTensor] = None,
        use_cache: Optional[bool] = None,
        output_attentions: Optional[bool] = None,
        output_hidden_states: Optional[bool] = None,
        return_dict: Optional[bool] = None,
    ) -> Union[Tuple, BaseModelOutputWithPastAndCrossAttentions]:
        outputs_list = []
        for i in range(len(self.decoder_list)):
            outputs_list.append(self.decoder_list[i].forward(
                                                     input_ids, attention_mask,
                                                     encoder_hidden_states[..., self.accum_hidden_nums[i]:self.accum_hidden_nums[i+1]],
                                                     encoder_attention_mask,
                                                     head_mask,
                                                     cross_attn_head_mask,
                                                     past_key_values[i] if past_key_values else None,
                                                     inputs_embeds,
                                                     use_cache,
                                                     output_attentions,
                                                     output_hidden_states,
                                                     return_dict))
        if not isinstance(outputs_list[0], tuple):
            outputs_list = [
                (outputs.last_hidden_state, outputs.past_key_values,
                 outputs.hidden_states, outputs.attentions, outputs.cross_attentions)
                for outputs in outputs_list
            ]
        merge_outputs = [[] for _ in range(len(outputs_list[0]))]
        for outputs in outputs_list:
            for idx, item in enumerate(outputs):
                merge_outputs[idx].append(item)
        merge_outputs = [tuple(item) for item in merge_outputs]
        if not return_dict:
            return tuple(merge_outputs)
        return BaseModelOutputWithPastAndCrossAttentions(
            last_hidden_state=merge_outputs[0],
            past_key_values=merge_outputs[1],
            hidden_states=merge_outputs[2],
            attentions=merge_outputs[3],
            cross_attentions=merge_outputs[4]
        )

